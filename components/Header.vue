<script setup>
	const typeObj = reactive({
		typeValue: "",
		typeStatus: false,
		typeArray: [
			"Welcome!",
			"Hola!",
			"你好!",
			"Bonjour!",
			"Hello!",
			"Ciao!",
			"こんにちは!",
			"안녕하세요!",
		],
		typingSpeed: 75,
		erasingSpeed: 75,
		newTextDelay: 1000,
		typeArrayIndex: 0,
		charIndex: 0,
	});

	function typeText() {
		if (typeObj.charIndex < typeObj.typeArray[typeObj.typeArrayIndex].length) {
			if (!typeObj.typeStatus) {
				typeObj.typeStatus = true;
			}
			typeObj.typeValue += typeObj.typeArray[typeObj.typeArrayIndex].charAt(
				typeObj.charIndex
			);
			typeObj.charIndex += 1;
			setTimeout(typeText, typeObj.typingSpeed);
		} else {
			typeObj.typeStatus = false;
			setTimeout(eraseText, typeObj.newTextDelay);
		}
	}

	function eraseText() {
		if (typeObj.charIndex > 0) {
			if (!typeObj.typeStatus) {
				typeObj.typeStatus = true;
			}
			typeObj.typeValue = typeObj.typeArray[typeObj.typeArrayIndex].substring(
				0,
				typeObj.charIndex - 1
			);
			typeObj.charIndex -= 1;
			setTimeout(eraseText, typeObj.erasingSpeed);
		} else {
			typeObj.typeStatus = false;
			typeObj.typeArrayIndex += 1;
			if (typeObj.typeArrayIndex >= typeObj.typeArray.length) {
				typeObj.typeArrayIndex = 0;
			}
			setTimeout(typeText, typeObj.typingSpeed + 1000);
		}
	}

	onMounted(() => {
		setTimeout(typeText, typeObj.newTextDelay + 200);
	});
</script>

